    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if(row>=0){
        int ok=JOptionPane.showConfirmDialog(null, "Yakin Mau Hapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if(ok==0){
        model.removeRow(row);
        }
      }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{
        jTextFieldKode.getText(), jTextFieldNama.getText(), jTextFieldHarga.getText(), jTextFieldStok.getText()});
        JOptionPane.showMessageDialog(null, "Data berhasil diubah dan Ditambahkan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.getSelectedRow();
        jTextFieldKode.setText(jTable1.getValueAt(baris, 0).toString());
        jTextFieldNama.setText(jTable1.getValueAt(baris, 1).toString());
        jTextFieldHarga.setText(jTable1.getValueAt(baris, 2).toString());
        jTextFieldStok.setText(jTable1.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

